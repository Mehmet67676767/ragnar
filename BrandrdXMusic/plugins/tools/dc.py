@app.on_message(filters.command(["d", "c"]))
async def dogruluk_cesaret(c: app, m: Message):
    command = m.command[0]

    if command == "d":
        return await m.reply_text(
            f"**✅ Doğruluk mu ? ?? Cesaret mi ?**\n\n"
            f"{m.from_user.mention} Doğruluk sorusu seçti !\n\n"
            f"{random.choice(dogruluk_listesi)}"
        )

    elif command == "c":
        return await m.reply_text(
            f"**✅ Doğruluk mu ? ?? Cesaret mi ?**\n\n"
            f"{m.from_user.mention} Cesaret sorusu seçti !\n\n"
            f"{random.choice(cesaret_listesi)}"
        )
